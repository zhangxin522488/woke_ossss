function createShowingTable(bb) {
	//获取后台传过来的jsonData,并进行解析  
	//此处需要让其动态的生成一个table并填充数据 
	if(bb.total == 0) {
		$("#tbbb").html("");
	} else {
		console.log(bb);
		var bb = JSON.parse(bb);
		var tableStr = "";
		var len = bb.length;
		if(len == 0) {
			var wuxiaoxi = '<p>' + "暂无内容" + '</p>'
			$("#tbbb").html(wuxiaoxi);
		} else {
			for(var i = 0; i < len; i++) {
				//				debugger
				if(bb[i].summary == undefined) {
					bb[i].summary = ""
				}
				if(bb[i].goldskind == 1) {
					bb[i].goldskind = '签到获得';
				}
				if(bb[i].goldskind == 2) {
					bb[i].goldskind = '邀请好友获得';
				}
				if(bb[i].goldskind == 3) {
					bb[i].goldskind = '参加活动获得';
				}
				if(bb[i].goldskind == 4) {
					bb[i].goldskind = '付款使用';
				}

				var num = parseInt(i) + parseInt(1);
				tableStr = tableStr + '<tr>' +
					'<th>' + num + '</th>' +
					'<th>' + bb[i].goldskind + '</th>' +
					'<th>' + bb[i].goldsamt + '</th>' +
					'<th>' + bb[i].goldsdate + '</th>' +
					//					'<th data-toggle="modal" data-target="#myModal" style="color:blue;cursor: pointer" ;><span class="glyphicon glyphicon-eye-open"></span></th>' +
					'<th data-toggle="modal" data-target="#myModal" style="color:blue;cursor: pointer" ;><span class="glyphicon glyphicon-edit"></span></th>' +
					'<th style="display: none;">' + bb[i].summary + '</th>' + //id

					'</tr>';
			}
			//将动态生成的table添加的事先隐藏的div中.  
			$("#tbbb").html(tableStr);
		}
		//点击详情
		$("#tbbb tr th:nth-child(5)").click(function() {
			$("#app_q_zhaiyao").html($(this).siblings().eq(4).html());

		});
	}
}

